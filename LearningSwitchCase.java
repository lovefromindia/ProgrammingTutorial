public class LearningSwitchCase {

    public static void main(String[] args) {

        String name = "Java";

        switch(name){

            case "Cpp":

                System.out.println("Cool Language");

                break;

            case "C":

                System.out.println("Old but Gold");

                break;

            case "Jav":

                System.out.println("Good one");

                break;

            default:

                System.out.println("Don't say HTML is a programming language");

        }

    }

}
